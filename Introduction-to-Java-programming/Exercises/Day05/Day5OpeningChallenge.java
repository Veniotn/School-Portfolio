public class Day5OpeningChallenge {
    public static void main (String []args) {
        int value1 = 2;
        int value2 = 3;
        int answer = multiply (value1, value2);
        System.out.println(answer);
    }



    public static int multiply(int num1, int num2) {
        int result = num1 * num2;
        return result;
    }
}